<?php
/*
 Дана задача:
    На школьной выставке 80 рисунков. 23 из них выполнены фломастерами, 40 карандашами, а остальные — красками. Сколько рисунков, выполненные красками, на школьной выставке?
    Описать и вывести условия, решение этой задачи на PHP. Все предоставленные числа из пункта 1 должны быть указаны в константах.
 */
const PICTURE = 80;
const PICTURE_MARKER = 23;
const PICTURE_PENCIL = 40;

$picture_paints = PICTURE - (PICTURE_MARKER + PICTURE_PENCIL);


echo '<b>Дано:</b><br>На школьной выставке <b>' . PICTURE . '</b> рисунков. <b>' . PICTURE_MARKER . '</b> из них выполнены фломастерами, <b>' . PICTURE_PENCIL . '</b> карандашами, а остальные — красками.<br>Сколько рисунков, выполненные красками, на школьной выставке?';
echo '<br><br><b>Решение:</b><br>';
echo PICTURE . ' - (' . PICTURE_MARKER . ' + ' . PICTURE_PENCIL . ') = ' . $picture_paints;
echo '<br><br><b>Ответ:</b> <br> На школьной выставке <b>' . $picture_paints . '</b> картин нрисованных красками.';
